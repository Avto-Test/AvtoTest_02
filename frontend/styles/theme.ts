function serializeTokens(selector: string, tokens: ThemeTokens) {
  const declarations = Object.entries(tokens)
    .map(([name, value]) => `  ${name}: ${value};`)
    .join("\n");

  return `${selector} {\n${declarations}\n}`;
}

export const themeStyleText = [
  serializeTokens(":root, .light", themeTokens.light),
  serializeTokens(".dark", themeTokens.dark),
].join("\n\n");
